  private static int find(int[] array) {
    int high = 1;
    while (array[high] != 0) {
      high = high * 2;
    }

    int low = high / 2;
    while (array[low] != 1) {
      low = low / 2;
    }

    while (low <= high) {
      int mid = (low + high) / 2;
      if (low == high) {
        return array[low];
      }
      if (array[mid - 1] == 1 && array[mid + 1] == 1) {
        low = mid + 1;
      } else if (array[mid - 1] == 0 && array[mid + 1] == 0) {
        high = mid - 1;
      } else if (array[mid - 1] == 1 && array[mid + 1] == 0) {
        return array[mid] == 0 ? mid : mid + 1;
      }
    }
    return 0;
  }
